CREATE TABLE city (
    city_id BIGINT GENERATED BY DEFAULT AS IDENTITY  PRIMARY KEY ,
    city_name VARCHAR(255),
    state_id BIGINT,
    created_at  TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    deleted_at  TIMESTAMP WITHOUT TIME ZONE,
    update_at  TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    city_status VARCHAR(20),
    CONSTRAINT fk_city_state FOREIGN KEY (state_id) REFERENCES state(state_id)
);